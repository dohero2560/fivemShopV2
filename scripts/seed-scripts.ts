async function seedScripts() {
  try {
    const client = await MongoClient.connect(uri)
    const db = client.db()

    // Delete existing scripts
    await db.collection("scripts").deleteMany({})

    // Insert sample scripts
    const result = await db.collection("scripts").insertMany(sampleScripts)

    console.log(`Successfully seeded ${result.insertedCount} scripts!`)
    await client.close()
    process.exit(0)
  } catch (error) {
    console.error("Error seeding scripts:", error)
    process.exit(1)
  }
}

